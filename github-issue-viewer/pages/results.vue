<template>
  <fragment>
    <header>
      <nuxt-link to="/" class="title">
        <h1>Github Issue Viewer</h1>
      </nuxt-link>
      <a class="repoURL" target="_blank" :href="repo | formatURL">
        <div>View on GitHub</div>
        <LinkIcon />
      </a>
    </header>
    <nav>
      <nuxt-link to="/">
        <CloseIcon class="close-icon" />
      </nuxt-link>
    </nav>
    <main>
      <IssueList />
    </main>
  </fragment>
</template>
<script>
import { mapState } from 'vuex'
import IssueList from '../components/IssueList'
import CloseIcon from '~/assets/icons/close.svg'
import LinkIcon from '~/assets/icons/link.svg'

export default {
  components: {
    CloseIcon,
    IssueList,
    LinkIcon
  },
  filters: {
    formatURL(repo) {
      return ` https://github.com/${repo.owner}/${repo.name}`
    }
  },
  computed: mapState({
    repo: (state) => state.issues.repo
  }),
  async fetch({ store, error }) {
    try {
      await store.dispatch('issues/fetchIssues')
    } catch (err) {
      error({
        statusCode: 503,
        message: 'Unable to retrieve issues at this time. Please try again.'
      })
    }
  }
}
</script>
